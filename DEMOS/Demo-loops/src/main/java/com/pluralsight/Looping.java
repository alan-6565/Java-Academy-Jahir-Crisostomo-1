package com.pluralsight;

public class Looping {
    public static void main(String[] args) {
        for(int i = 1; i < 11; i++) {

            System.out.println(i + ". I will be a good developer");
        }

        for(int i = 81; i > 0; i--) {
            System.out.println(i + " more days left of Year Up Code academy");
        }
    }
}
